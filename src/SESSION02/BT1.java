package SESSION02;

/*
Kiểm tra User có phải Admin
→ Predicate<User>
Lý do: Dùng để kiểm tra điều kiện và trả về true/false.

Chuyển User thành chuỗi username
→ Function<User, String>
Lý do: Dùng để chuyển đổi dữ liệu từ User sang String.

In thông tin User ra Console
→ Consumer<User>
Lý do: Dùng để thực hiện hành động với dữ liệu, không trả về giá trị.

Khởi tạo User mới với giá trị mặc định
→ Supplier<User>
Lý do: Dùng để tạo và cung cấp dữ liệu, không cần tham số đầu vào.
* */
